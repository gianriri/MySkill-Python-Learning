# Operator penjumlahan
a = 6
b = 4

c = a+ b
print(c)

# Operator Penggabungan (hanya string)
a = "10" + "gabung"
print(a)

# Operator Penugasan / Assignment
a = 2
a += 1 # sama dengan a = a + 1
print(a)

# Operator relasi atau perbandingan
a = 5
b = 10
print (a < b)

c = 15
d = 10
print (c > d)

x = 5
y = 5
print (x == y)

p = 7
q = 5
print (p != q)

# Operator Logika
a = True
b = False
result = a or b
print(result)

c = True
d = False
result = c and d
print(result)

e = True
result = not e
print(result)